#pragma once

#include <glm/glm.hpp>
#include <glm/gtc/quaternion.hpp>

// Vector types
typedef glm::vec2 Vector2;
typedef glm::vec3 Vector3;
typedef glm::vec4 Vector4;

// Matrix types
typedef glm::mat2 Matrix2;
typedef glm::mat3 Matrix3;
typedef glm::mat4 Matrix4;

// Quaternion type
typedef glm::quat Quaternion;

// Integer vector types
typedef glm::ivec2 IntVector2;
typedef glm::ivec3 IntVector3;
typedef glm::ivec4 IntVector4;

// Unsigned integer vector types
typedef glm::uvec2 UIntVector2;
typedef glm::uvec3 UIntVector3;
typedef glm::uvec4 UIntVector4;

// Double precision vector types
typedef glm::dvec2 DoubleVector2;
typedef glm::dvec3 DoubleVector3;
typedef glm::dvec4 DoubleVector4;

// Double precision matrix types
typedef glm::dmat2 DoubleMatrix2;
typedef glm::dmat3 DoubleMatrix3;
typedef glm::dmat4 DoubleMatrix4;